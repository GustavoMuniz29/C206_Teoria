public class Corpo {
    private  String tipo;
    private  String cor;



    //Getters and Setters
    public String getTipo() { //Esse get permite acessar o tipo de corpo do robo
        return tipo;
    }

    public void setTipo(String tipo) { //Esse set permite especificar o tipo de corpo do robo
        this.tipo = tipo;
    }

    public String getCor() { //Esse get permite acessar a cor do robo
        return cor;
    }

    public void setCor(String cor) { //Esse get permite especificar a cor do robo
        this.cor = cor;
    }
}
